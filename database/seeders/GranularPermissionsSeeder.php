            'إدارة المواد الدراسية' => ['عرض المواد الدراسية', 'إضافة مادة دراسية', 'تعديل مادة دراسية', 'حذف مادة دراسية'],
            'إدارة الموظفين' => ['عرض الموظفين', 'إضافة موظف', 'تعديل موظف', 'حذف موظف'],
            'إدارة قوالب التقارير' => ['عرض قوالب التقارير', 'إضافة قالب تقرير', 'تعديل قالب تقرير', 'حذف قالب تقرير'],
            // الإجازات والعطلات تعتمد غالباً على إدارة الموظفين، لكن يمكن تفصيلها هنا
            'إدارة الإجازات والعطلات' => ['عرض الإجازات والعطلات', 'إضافة إجازة أو عطلة', 'تعديل إجازة أو عطلة', 'حذف إجازة أو عطلة'],
            'إدارة الزيارات الصفية' => ['عرض الزيارات الصفية', 'إضافة زيارة صفية', 'تعديل زيارة صفية', 'حذف زيارة صفية', 'اعتماد زيارة صفية', 'عرض زياراتي الصفية'],
        ];

        // 1. إنشاء الصلاحيات الجديدة إذا لم تكن موجودة
        foreach ($permissionsMap as $parent => $children) {
            $parentPerm = Permission::where('name', $parent)->first();
            $moduleName = $parentPerm ? $parentPerm->module : 'general';

            foreach ($children as $child) {
                Permission::firstOrCreate(
                    ['name' => $child],
                    ['module' => $moduleName]
                );
            }
        }

        // 2. إسناد الصلاحيات التفصيلية للأدوار بناءً على الصلاحية العامة
        $roles = Role::with('permissions')->get();

        foreach ($roles as $role) {
            $rolePermissionNames = $role->permissions->pluck('name')->toArray();
            $newPermissionsToAttach = [];

            foreach ($permissionsMap as $parent => $children) {
                // إذا كان الدور يمتلك الصلاحية العامة
                if (in_array($parent, $rolePermissionNames)) {
                    foreach ($children as $child) {
                        if (!in_array($child, $rolePermissionNames)) {
                            // نجلب الـ id الخاص بالصلاحية
                            $perm = Permission::where('name', $child)->first();
                            if ($perm) {
                                $newPermissionsToAttach[] = $perm->id;
                            }
                        }
                    }
                }
            }

            // نضيف الإجازات والعطلات لمن لديهم إدارة الموظفين (لأنها كانت تندرج تحتها)
            if (in_array('إدارة الموظفين', $rolePermissionNames)) {
                $leavePermissions = ['عرض الإجازات والعطلات', 'إضافة إجازة أو عطلة', 'تعديل إجازة أو عطلة', 'حذف إجازة أو عطلة'];
                foreach ($leavePermissions as $lp) {
                    $perm = Permission::where('name', $lp)->first();
                    if ($perm) {
                        $newPermissionsToAttach[] = $perm->id;
                    }
                }
            }

            if (!empty($newPermissionsToAttach)) {
                $role->permissions()->syncWithoutDetaching(array_unique($newPermissionsToAttach));
            }
        }
        
        $this->command->info('تم إنشاء الصلاحيات التفصيلية وربطها بنجاح!');
    }
}
