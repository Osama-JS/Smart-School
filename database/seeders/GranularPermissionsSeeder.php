<?php

namespace Database\Seeders;

use Illuminate\Database\Seeder;
use App\Models\Permission;
use App\Models\Role;

class GranularPermissionsSeeder extends Seeder
{
    /**
     * Run the database seeds.
     */
    public function run(): void
    {
        $permissionsMap = [
            'إدارة أنواع المخالفات' => ['عرض أنواع المخالفات', 'إضافة نوع مخالفة', 'تعديل نوع مخالفة', 'حذف نوع مخالفة'],
            'إدارة الأقسام' => ['عرض الأقسام', 'إضافة قسم', 'تعديل قسم', 'حذف قسم'],
            'إدارة الاجتماعات' => ['عرض الاجتماعات', 'إضافة اجتماع', 'تعديل اجتماع', 'حذف اجتماع', 'تحضير الاجتماع'],
            'إدارة التسجيلات' => ['عرض التسجيلات', 'إضافة تسجيل', 'تعديل تسجيل', 'حذف تسجيل'],
            'إدارة التقارير' => ['عرض التقارير', 'إضافة تقرير', 'تعديل تقرير', 'حذف تقرير'],
            'إدارة الجداول الدراسية' => ['عرض الجداول الدراسية', 'إضافة جدول دراسي', 'تعديل جدول دراسي', 'حذف جدول دراسي', 'إسناد المعلمين'],
            'إدارة الحضور والانصراف' => ['عرض الحضور والانصراف', 'إضافة حضور وانصراف', 'تعديل حضور وانصراف', 'حذف حضور وانصراف', 'اعتماد الحضور'],
            'إدارة الدرجات الوظيفية' => ['عرض الدرجات الوظيفية', 'إضافة درجة وظيفية', 'تعديل درجة وظيفية', 'حذف درجة وظيفية'],
            'إدارة السنوات الدراسية' => ['عرض السنوات الدراسية', 'إضافة سنة دراسية', 'تعديل سنة دراسية', 'حذف سنة دراسية'],
            'إدارة الشفتات' => ['عرض الشفتات', 'إضافة شفت', 'تعديل شفت', 'حذف شفت'],
            'إدارة الصلاحيات' => ['عرض الصلاحيات', 'إضافة صلاحية', 'تعديل صلاحية', 'حذف صلاحية'],
            'إدارة الطلاب' => ['عرض الطلاب', 'إضافة طالب', 'تعديل طالب', 'حذف طالب'],
            'إدارة الفروع' => ['عرض الفروع', 'إضافة فرع', 'تعديل فرع', 'حذف فرع'],
            'إدارة المخالفات' => ['عرض المخالفات', 'إضافة مخالفة', 'تعديل مخالفة', 'حذف مخالفة'],
            'إدارة المراحل والصفوف' => ['عرض المراحل والصفوف', 'إضافة مرحلة أو صف', 'تعديل مرحلة أو صف', 'حذف مرحلة أو صف'],
            'إدارة المستخدمين' => ['عرض المستخدمين', 'إضافة مستخدم', 'تعديل مستخدم', 'حذف مستخدم'],
